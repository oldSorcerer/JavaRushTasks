package com.javarush.task.task04.task0433;


/* 
Гадание на долларовый счет
*/

import java.io.*;

public class Solution {
    public static void main(String[] args){

        int i = 0;
        while (i < 10) {
            int y = 0;
            while (y < 10) {
                System.out.print("S");
                y++;
            }
            System.out.println();
            i++;
        }

    }
}
