package com.javarush.task.pro.task04.task0404;

/* 
Заполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {

        int i = 0;
        while (i < 5) {
            int j = 0;
            while (j < 10) {
                System.out.print("Q");
                j++;
            }
            System.out.println();
            i++;
        }
    }
}